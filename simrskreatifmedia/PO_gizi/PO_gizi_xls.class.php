              $this->prep_modulos("Res_xls");
          } 
          $this->Arquivo    = "sc_xls";
          $this->Arquivo   .= "_" . date("YmdHis") . "_" . rand(0, 1000);
          $this->Arq_zip    = $this->Arquivo . "_PO_gizi.zip";
          $this->Arquivo   .= "_PO_gizi" . $this->Xls_tp;
          $this->Tit_doc    = "PO_gizi" . $this->Xls_tp;
          $this->Tit_zip    = "PO_gizi.zip";
