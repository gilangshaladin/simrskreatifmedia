          }
          if ($this->Tem_xls_res)
          { 
              require_once($this->Ini->path_aplicacao . "grid_select_ranap_res_xls.class.php");
              $this->Res_xls = new grid_select_ranap_res_xls();
              $this->prep_modulos("Res_xls");
